package day02;

public class Test05 {
	public static void main(String[] args) {
		//3.14를 float 타입 변수 pi에 기억해보자.
		
		float pi ; // 변수 선언
		
		pi = 3.14f; // 3.14 뒤에 f를 붙임 ==> 리터럴 형변환
	}
	
	/*
	 	참고 ]
	 		개발자가 입력하는 정수형 데이터는
	 		특별한 조작없이 모든 데이터형이 기억될 수 있다.
	 		
	 		예 ]
	 			int a = 100;
	 			byte ab = 100;
	 			short c = 100;
	 			long d = 100;
	 			
	 			정수형은 크게 문제가 되는 경우가 없다.
	 			실수형의 float 가 주의 필요.
	 */
}
